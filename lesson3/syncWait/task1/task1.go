package main

import (
	"fmt"
	"sync"
)

func main() {
	cnt := 100
	var wg sync.WaitGroup

	for i := 0; i < cnt; i++ {
		wg.Add(1)
		go func(id int) {
			defer wg.Done()
			fmt.Println(id)
		}(i) // передаём i как аргумент, иначе все горутины увидят одно и то же значение
	}

	wg.Wait() // main ждёт завершения всех 100 горутин

	// Было две ошибки в исходном коде:
	//   1. go func() { fmt.Println(i) }(): замыкание на переменную цикла i;
	//      к моменту запуска горутин i уже равно 100, все печатают 100.
	//   2. Не было wg.Wait(): main завершался раньше, чем успевали отработать горутины.
	//
	// Исправление: передать i в горутину как id и дождаться всех через WaitGroup.
	// Вывод: числа от 0 до 99 (порядок строк может быть любым: горутины параллельны).
}
